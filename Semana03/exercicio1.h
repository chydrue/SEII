void soma(a, b)
{
	s = a + b;
	printf("A soma dos números é: %f: ", s);
	return;
}

void sub(a, b)
{
	s = a - b;
	printf("A subtração dos números é: %f", s);
	return 0;
}

void mult(a, b)
{
	m = a * b;
	printf("A multiplicação dos números é: %f", m);
	return;
}

void div(a, b)
{
	d = a / b;
	printf("A divisão dos números é: %f", d);
	return;
}